  public Optional<CreditCouponPaymentPeriod> findPeriod(LocalDate date) {
    return periodicPayments.stream()
        .filter(p -> p.contains(date))
        .reduce(ensureOnlyOne());
  }

  public double accruedInterest(LocalDate referenceDate) {
    if (referenceDate.isBefore(getStartDate())) {
      return 0d;
    }
    double coupon = getCoupon();
    CreditCouponPaymentPeriod period = findPeriod(referenceDate)
        .orElseThrow(() -> new IllegalArgumentException("Date outside range"));
    return dayCount.relativeYearFraction(period.getStartDate(), referenceDate) * coupon;
  }

